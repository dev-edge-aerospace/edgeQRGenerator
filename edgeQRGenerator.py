#! /bin/python3
import argparse
import base64
import io
from PIL import Image

# Try importing qrcode; if unavailable, instruct how to install it in a venv and exit.
try:
    import qrcode
except ImportError:
    msg = (
        "The 'qrcode' package is required to run this script.\n"
        "Create and activate a virtual environment, then install dependencies:\n\n"
        "python3 -m venv .venv\n"
        "source .venv/bin/activate\n"
        "pip install qrcode pillow\n\n"
        "After installing, re-run this script."
    )
    print(msg)
    raise SystemExit(1)


# Percentage of the QR code's smaller dimension to use for overlay size (0-100)
OVERLAY_SIZE_PERCENT = 20


def generate_qr_code(data, filename='qrcode.png'):
    qr = qrcode.QRCode(
        version=1,
        error_correction=qrcode.constants.ERROR_CORRECT_H,
        box_size=10,
        border=4,
    )
    qr.add_data(data)
    qr.make(fit=True)

    img = qr.make_image(fill_color="black", back_color="white")
    # Ensure image is RGBA so we can composite an overlay with transparency
    img = img.convert("RGBA")

    # Decode overlay image from base64 and composite it centered on the QR code
    try:
        overlay_data = base64.b64decode(OVERLAY_PNG)
        overlay = Image.open(io.BytesIO(overlay_data)).convert("RGBA")

        # Compute overlay size based on the QR image size and the percentage constant
        scale = OVERLAY_SIZE_PERCENT / 100.0
        max_dim = int(min(img.width, img.height) * scale)
        if max_dim > 0:
            overlay.thumbnail((max_dim, max_dim), Image.LANCZOS)
            pos = ((img.width - overlay.width) // 2, (img.height - overlay.height) // 2)
            img.paste(overlay, pos, overlay)
    except Exception:
        # If overlay decoding/compositing fails, continue and save QR only
        print("Warning: Could not apply overlay image to QR code.")
        pass

    img.save(filename)
    print(f"QR code saved as {filename}")

def main():
    parser = argparse.ArgumentParser(description="Generate a QR code image from data")
    parser.add_argument('data', help='Data/text to encode in the QR code')
    parser.add_argument('-o', '--output', help='Output filename', default='qrcode.png')
    args = parser.parse_args()

    data = args.data
    output_filename = args.output
    generate_qr_code(data, output_filename)
    # Optional: Display the generated QR code
    img = Image.open(output_filename)
    img.show()


